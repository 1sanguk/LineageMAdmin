    /// <summary>누적 경험치로 현재 레벨 계산</summary>
    public static int GetLevel(long experience)
    {
        for (int i = 0; i < 89; i++) // Thresholds[0..88]이 레벨 경계
            if (experience < Thresholds[i]) return i + 1;
        return MaxLevel; // >= Thresholds[88] 이면 레벨 90
    }

    /// <summary>해당 레벨 시작 누적 XP (레벨1 = 0)</summary>
    public static long GetStartXP(int level)
    {
        if (level <= 1) return 0;
        return Thresholds[level - 2];
    }

    /// <summary>해당 레벨의 MaxXP (다음 레벨 진입점 또는 레벨90 표시용 최대값)</summary>
    public static long GetMaxXP(int level)
    {
        return Thresholds[Math.Clamp(level - 1, 0, 89)];
    }
}
